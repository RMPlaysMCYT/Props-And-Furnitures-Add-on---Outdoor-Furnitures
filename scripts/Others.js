export function itemDecreased(player, inCreative = false, amount = 1) {
  if (isCreative(player) && !inCreative) return;
  const inventory = player.getComponent("inventory").container;
  const selectedSlotIndex = player.selectedSlotIndex;
  const item = inventory.getItem(selectedSlotIndex);
  if (!item) return;
  if (item.amount > amount) {
    item.amount -= amount;
    inventory.setItem(selectedSlotIndex, item);
  } else {
    inventory.setItem(selectedSlotIndex, undefined);
  }
}

export function BlockTypeVariants(blockId) {
  if (blockId.includes("cherry")) return "cherry";
  if (blockId.includes("bamboo")) return "bamboo";
  if (blockId.includes("copper")) return "copper";
  if (blockId.includes("crimson") || blockId.includes("warped")) return "nether";
  return "default";
}

